/* Set the width of the sidebar to 250px (show it) */
function openNav() {
  document.getElementById("navPanel").style.width = "250px";
}

/* Set the width of the sidebar to 0 (hide it) */
function closeNav() {
  document.getElementById("navPanel").style.width = "0";
} 